using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassAndObject.OperasiMatematika
{
    public class Kalkulator
    {
        public double Tambah(double d, double n)
        {
            return d + n;
        }

        public double Kurang(double d, double n)
        {
            return d - n;
        }

        public double Kali(double d, double n)
        {
            return d * n;
        }

        public double Bagi(double d, double n)
        {
            return d / n;
        }
    }
}
